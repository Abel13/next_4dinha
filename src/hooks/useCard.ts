export default function useCard() {
  const shuffledCards = () => {
    const shuffle = cards
      .filter((c) => c.symbol !== CardSymbol.Hide)
      .sort(() => Math.random() - 0.5);
    return shuffle;
  };

  const allCards = () => {
    return cards;
  };

  const getCard = (id?: number) => {
    if (id === undefined) return undefined;
    return cards.find((card) => card.id === id);
  };

  return {
    shuffledCards,
    allCards,
    getCard,
  };
}
